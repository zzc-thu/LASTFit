!   variations   of useful results: cv
!   with respect to varying inputs: u_tau rho_tau u v cv rho p_tau
! this subroutine is used to calculate the flux in 3d
SUBROUTINE UTOCV_TAU1_D_un(rho, rhod, u, ud, v, vd, w, rho_tau, rho_taud, &
& u_tau, u_taud, v_tau, w_tau, p_tau, p_taud, cv, cvd, gamma)
