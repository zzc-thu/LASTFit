!  Differentiation of utoflux_cpg3d1 in forward (tangent) mode:
!   variations   of useful results: f g h
!   with respect to varying inputs: f g h p u v w rho
SUBROUTINE UTOFLUX_CPG3D1_D_un(rho, rhod, u, ud, v, vd, w, wd, p, pd, f, fd&
& , g, gd, h, hd, gamma)
  IMPLICIT NONE
  REAL*8, INTENT(IN) :: rho, u, v, w, p, gamma
  REAL*8, INTENT(IN) :: rhod, ud, vd, wd, pd
  REAL*8, INTENT(OUT) :: f(5), g(5), h(5)
  REAL*8, INTENT(OUT) :: fd(5), gd(5), hd(5)
  REAL*8 :: temp
  REAL*8 :: temp0
  fd(1) = u*rhod + rho*ud
  f(1) = rho*u
  fd(2) = u**2*rhod + rho*2*u*ud + pd
  f(2) = rho*u*u + p
  fd(3) = v*(u*rhod+rho*ud) + rho*u*vd
  f(3) = rho*u*v
  fd(4) = w*(u*rhod+rho*ud) + rho*u*wd
