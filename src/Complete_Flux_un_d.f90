  REAL*8, INTENT(IN) :: rho, u, v, w, rho_tau, u_tau, v_tau, w_tau, &
& p_tau, gamma
  REAL*8, INTENT(IN) :: rhod, ud, vd, rho_taud, u_taud, p_taud
  REAL*8, INTENT(OUT) :: cv(5)
  REAL*8, INTENT(OUT) :: cvd(5)
  REAL*8 :: temp
  REAL*8 :: temp0
  cvd(1) = rho_taud
  cv(1) = rho_tau
  cvd(2) = u_tau*rhod + rho*u_taud + rho_tau*ud + u*rho_taud
  cv(2) = rho*u_tau + u*rho_tau
  cvd(3) = v_tau*rhod + rho_tau*vd + v*rho_taud
  cv(3) = rho*v_tau + v*rho_tau
  cvd(4) = w_tau*rhod + w*rho_taud
  cv(4) = rho*w_tau + w*rho_tau
  temp = w*w + u*u + v*v
  temp0 = w_tau*w + u_tau*u + v_tau*v
  cvd(5) = p_taud/(gamma-1.d0) + 0.5d0*(temp*rho_taud+rho_tau*(2*u*ud+2*&
&   v*vd)) + temp0*rhod + rho*(u*u_taud+u_tau*ud+v_tau*vd)
  cv(5) = p_tau/(gamma-1.d0) + 0.5d0*(rho_tau*temp) + rho*temp0
