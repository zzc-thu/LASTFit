&         hetadzeta(ic, jc, kc)
        xzetatau = (wallnormalxdzeta(ic, kc)*shockv(ic, kc)+wallnormalx(&
&         ic, kc)*shockvdzeta(ic, kc))*heta(ic, jc, kc) + wallnormalx(ic&
&         , kc)*shockv(ic, kc)*hetadzeta(ic, jc, kc)
        dydzetad(ic, jc, kc) = heta(ic, jc, kc)*(wallnormalydzeta(ic, kc&
&         )*shockhd(ic, kc)+wallnormaly(ic, kc)*shockhdzetad(ic, kc)) + &
&         hetadzeta(ic, jc, kc)*wallnormaly(ic, kc)*shockhd(ic, kc)
        dydzeta(ic, jc, kc) = wallsydzeta(ic, kc) + (wallnormalydzeta(ic&
&         , kc)*shockh(ic, kc)+wallnormaly(ic, kc)*shockhdzeta(ic, kc))*&
&         heta(ic, jc, kc) + wallnormaly(ic, kc)*shockh(ic, kc)*&
&         hetadzeta(ic, jc, kc)
        yzetatau = (wallnormalydzeta(ic, kc)*shockv(ic, kc)+wallnormaly(&
&         ic, kc)*shockvdzeta(ic, kc))*heta(ic, jc, kc) + wallnormaly(ic&
&         , kc)*shockv(ic, kc)*hetadzeta(ic, jc, kc)
        dzdzetad(ic, jc, kc) = heta(ic, jc, kc)*(wallnormalzdzeta(ic, kc&
&         )*shockhd(ic, kc)+wallnormalz(ic, kc)*shockhdzetad(ic, kc)) + &
&         hetadzeta(ic, jc, kc)*wallnormalz(ic, kc)*shockhd(ic, kc)
        dzdzeta(ic, jc, kc) = wallszdzeta(ic, kc) + (wallnormalzdzeta(ic&
&         , kc)*shockh(ic, kc)+wallnormalz(ic, kc)*shockhdzeta(ic, kc))*&
&         heta(ic, jc, kc) + wallnormalz(ic, kc)*shockh(ic, kc)*&
&         hetadzeta(ic, jc, kc)
        zzetatau = (wallnormalzdzeta(ic, kc)*shockv(ic, kc)+wallnormalz(&
&         ic, kc)*shockvdzeta(ic, kc))*heta(ic, jc, kc) + wallnormalz(ic&
&         , kc)*shockv(ic, kc)*hetadzeta(ic, jc, kc)
        temp0 = dydeta(ic, jc, kc)*dzdzeta(ic, jc, kc) - dzdeta(ic, jc, &
&         kc)*dydzeta(ic, jc, kc)
        temp1 = dydzeta(ic, jc, kc)*dzdxi(ic, jc, kc) - dydxi(ic, jc, kc&
&         )*dzdzeta(ic, jc, kc)
        temp2 = dydxi(ic, jc, kc)*dzdeta(ic, jc, kc) - dydeta(ic, jc, kc&
&         )*dzdxi(ic, jc, kc)
        tempd = temp0*dxdxid(ic, jc, kc) + dxdxi(ic, jc, kc)*(dzdzeta(ic&
&         , jc, kc)*dydetad(ic, jc, kc)+dydeta(ic, jc, kc)*dzdzetad(ic, &
&         jc, kc)-dydzeta(ic, jc, kc)*dzdetad(ic, jc, kc)-dzdeta(ic, jc&
&         , kc)*dydzetad(ic, jc, kc)) + temp1*dxdetad(ic, jc, kc) + &
&         dxdeta(ic, jc, kc)*(dzdxi(ic, jc, kc)*dydzetad(ic, jc, kc)+&
&         dydzeta(ic, jc, kc)*dzdxid(ic, jc, kc)-dzdzeta(ic, jc, kc)*&
&         dydxid(ic, jc, kc)-dydxi(ic, jc, kc)*dzdzetad(ic, jc, kc)) + &
&         temp2*dxdzetad(ic, jc, kc) + dxdzeta(ic, jc, kc)*(dzdeta(ic, &
&         jc, kc)*dydxid(ic, jc, kc)+dydxi(ic, jc, kc)*dzdetad(ic, jc, &
&         kc)-dzdxi(ic, jc, kc)*dydetad(ic, jc, kc)-dydeta(ic, jc, kc)*&
&         dzdxid(ic, jc, kc))
        temp = dxdxi(ic, jc, kc)*temp0 + dxdeta(ic, jc, kc)*temp1 + &
&         dxdzeta(ic, jc, kc)*temp2
        jacod(ic, jc, kc) = -(tempd/temp**2)
        jaco(ic, jc, kc) = 1.0d0/temp
        invjacodt(ic, jc, kc) = xxitau*(dydeta(ic, jc, kc)*dzdzeta(ic, &
&         jc, kc)-dzdeta(ic, jc, kc)*dydzeta(ic, jc, kc)) + xetatau*(&
&         dydzeta(ic, jc, kc)*dzdxi(ic, jc, kc)-dydxi(ic, jc, kc)*&
&         dzdzeta(ic, jc, kc)) + xzetatau*(dydxi(ic, jc, kc)*dzdeta(ic, &
&         jc, kc)-dydeta(ic, jc, kc)*dzdxi(ic, jc, kc)) + dxdxi(ic, jc, &
&         kc)*(yetatau*dzdzeta(ic, jc, kc)-zetatau*dydzeta(ic, jc, kc)) &
&         + dxdeta(ic, jc, kc)*(yzetatau*dzdxi(ic, jc, kc)-yxitau*&
&         dzdzeta(ic, jc, kc)) + dxdzeta(ic, jc, kc)*(yxitau*dzdeta(ic, &
&         jc, kc)-yetatau*dzdxi(ic, jc, kc)) + dxdxi(ic, jc, kc)*(dydeta&
&         (ic, jc, kc)*zzetatau-dzdeta(ic, jc, kc)*yzetatau) + dxdeta(ic&
&         , jc, kc)*(dydzeta(ic, jc, kc)*zxitau-dydxi(ic, jc, kc)*&
&         zzetatau) + dxdzeta(ic, jc, kc)*(dydxi(ic, jc, kc)*zetatau-&
&         dydeta(ic, jc, kc)*zxitau)
        dxidx(ic, jc, kc) = jaco(ic, jc, kc)*(dydeta(ic, jc, kc)*dzdzeta&
&         (ic, jc, kc)-dydzeta(ic, jc, kc)*dzdeta(ic, jc, kc))
        dxidy(ic, jc, kc) = -(jaco(ic, jc, kc)*(dxdeta(ic, jc, kc)*&
&         dzdzeta(ic, jc, kc)-dzdeta(ic, jc, kc)*dxdzeta(ic, jc, kc)))
        dxidz(ic, jc, kc) = jaco(ic, jc, kc)*(dxdeta(ic, jc, kc)*dydzeta&
&         (ic, jc, kc)-dydeta(ic, jc, kc)*dxdzeta(ic, jc, kc))
        temp2 = dydxi(ic, jc, kc)*dzdzeta(ic, jc, kc) - dzdxi(ic, jc, kc&
&         )*dydzeta(ic, jc, kc)
        detadxd(ic, jc, kc) = -(temp2*jacod(ic, jc, kc)+jaco(ic, jc, kc)&
&         *(dzdzeta(ic, jc, kc)*dydxid(ic, jc, kc)+dydxi(ic, jc, kc)*&
&         dzdzetad(ic, jc, kc)-dydzeta(ic, jc, kc)*dzdxid(ic, jc, kc)-&
&         dzdxi(ic, jc, kc)*dydzetad(ic, jc, kc)))
        detadx(ic, jc, kc) = -(jaco(ic, jc, kc)*temp2)
        temp2 = dxdxi(ic, jc, kc)*dzdzeta(ic, jc, kc) - dzdxi(ic, jc, kc&
&         )*dxdzeta(ic, jc, kc)
        detadyd(ic, jc, kc) = temp2*jacod(ic, jc, kc) + jaco(ic, jc, kc)&
&         *(dzdzeta(ic, jc, kc)*dxdxid(ic, jc, kc)+dxdxi(ic, jc, kc)*&
&         dzdzetad(ic, jc, kc)-dxdzeta(ic, jc, kc)*dzdxid(ic, jc, kc)-&
&         dzdxi(ic, jc, kc)*dxdzetad(ic, jc, kc))
        detady(ic, jc, kc) = jaco(ic, jc, kc)*temp2
        temp2 = dxdxi(ic, jc, kc)*dydzeta(ic, jc, kc) - dydxi(ic, jc, kc&
&         )*dxdzeta(ic, jc, kc)
        detadzd(ic, jc, kc) = -(temp2*jacod(ic, jc, kc)+jaco(ic, jc, kc)&
&         *(dydzeta(ic, jc, kc)*dxdxid(ic, jc, kc)+dxdxi(ic, jc, kc)*&
&         dydzetad(ic, jc, kc)-dxdzeta(ic, jc, kc)*dydxid(ic, jc, kc)-&
&         dydxi(ic, jc, kc)*dxdzetad(ic, jc, kc)))
        detadz(ic, jc, kc) = -(jaco(ic, jc, kc)*temp2)
        dzetadx(ic, jc, kc) = jaco(ic, jc, kc)*(dydxi(ic, jc, kc)*dzdeta&
&         (ic, jc, kc)-dzdxi(ic, jc, kc)*dydeta(ic, jc, kc))
        dzetady(ic, jc, kc) = -(jaco(ic, jc, kc)*(dxdxi(ic, jc, kc)*&
&         dzdeta(ic, jc, kc)-dzdxi(ic, jc, kc)*dxdeta(ic, jc, kc)))
        dzetadz(ic, jc, kc) = jaco(ic, jc, kc)*(dxdxi(ic, jc, kc)*dydeta&
&         (ic, jc, kc)-dydxi(ic, jc, kc)*dxdeta(ic, jc, kc))
        arg1 = dxidx(ic, jc, kc)*dxidx(ic, jc, kc) + dxidy(ic, jc, kc)*&
&         dxidy(ic, jc, kc) + dxidz(ic, jc, kc)*dxidz(ic, jc, kc)
        nablaxi(ic, jc, kc) = SQRT(arg1)
        arg1d = 2*detadx(ic, jc, kc)*detadxd(ic, jc, kc) + 2*detady(ic, &
&         jc, kc)*detadyd(ic, jc, kc) + 2*detadz(ic, jc, kc)*detadzd(ic&
&         , jc, kc)
        arg1 = detadx(ic, jc, kc)*detadx(ic, jc, kc) + detady(ic, jc, kc&
&         )*detady(ic, jc, kc) + detadz(ic, jc, kc)*detadz(ic, jc, kc)
        temp2 = SQRT(arg1)
        IF (arg1 .EQ. 0.0) THEN
          nablaetad(ic, jc, kc) = 0.0_8
        ELSE
          nablaetad(ic, jc, kc) = arg1d/(2.0*temp2)
        END IF
        nablaeta(ic, jc, kc) = temp2
        arg1 = dzetadx(ic, jc, kc)*dzetadx(ic, jc, kc) + dzetady(ic, jc&
&         , kc)*dzetady(ic, jc, kc) + dzetadz(ic, jc, kc)*dzetadz(ic, jc&
&         , kc)
        nablazeta(ic, jc, kc) = SQRT(arg1)
      END DO
    END DO
  END DO
  jc = ny_local
  DO kc=1-overlap,nz_local+overlap
    DO ic=1-overlap,nx_local+overlap
      temp2 = detadx(ic, jc, kc)/nablaeta(ic, jc, kc)
      shocknormalxd(ic, kc) = (detadxd(ic, jc, kc)-temp2*nablaetad(ic, &
&       jc, kc))/nablaeta(ic, jc, kc)
      shocknormalx(ic, kc) = temp2
      temp2 = detady(ic, jc, kc)/nablaeta(ic, jc, kc)
      shocknormalyd(ic, kc) = (detadyd(ic, jc, kc)-temp2*nablaetad(ic, &
&       jc, kc))/nablaeta(ic, jc, kc)
      shocknormaly(ic, kc) = temp2
      temp2 = detadz(ic, jc, kc)/nablaeta(ic, jc, kc)
      shocknormalzd(ic, kc) = (detadzd(ic, jc, kc)-temp2*nablaetad(ic, &
&       jc, kc))/nablaeta(ic, jc, kc)
      shocknormalz(ic, kc) = temp2
    END DO
  END DO
  
  call Parallel_Exchange_Surface(shocknormalx)
  call Parallel_Exchange_Surface(shocknormaly)
  call Parallel_Exchange_Surface(shocknormalz)
  call Parallel_Exchange_Surface(shocknormalxd)
  call Parallel_Exchange_Surface(shocknormalyd)
  call Parallel_Exchange_Surface(shocknormalzd)
  
! Standing for the Shock Surface
  jc = ny_local
  srhod = 0.0_8
  spd = 0.0_8
  std = 0.0_8
  sud = 0.0_8
  svd = 0.0_8
  swd = 0.0_8
  DO kc=1-overlap,nz_local+overlap
    DO ic=1-overlap,nx_local+overlap
!Velocity ahead of the shocks
      temp2 = wallnormalx(ic, kc)*shocknormalx(ic, kc) + wallnormaly(ic&
&       , kc)*shocknormaly(ic, kc) + wallnormalz(ic, kc)*shocknormalz(ic&
&       , kc)
      delta_infd = shocknormalx(ic, kc)*u_freed(ic, jc, kc) + u_free(ic&
&       , jc, kc)*shocknormalxd(ic, kc) + shocknormaly(ic, kc)*v_freed(&
&       ic, jc, kc) + v_free(ic, jc, kc)*shocknormalyd(ic, kc) + &
&       shocknormalz(ic, kc)*w_freed(ic, jc, kc) + w_free(ic, jc, kc)*&
&       shocknormalzd(ic, kc) - temp2*shockvd(ic, kc) - shockv(ic, kc)*(&
&       wallnormalx(ic, kc)*shocknormalxd(ic, kc)+wallnormaly(ic, kc)*&
&       shocknormalyd(ic, kc)+wallnormalz(ic, kc)*shocknormalzd(ic, kc))
      delta_inf = u_free(ic, jc, kc)*shocknormalx(ic, kc) + v_free(ic, &
&       jc, kc)*shocknormaly(ic, kc) + w_free(ic, jc, kc)*shocknormalz(&
&       ic, kc) - shockv(ic, kc)*temp2
      temp2 = (gamma+1.d0)*delta_inf*rho_free(ic, jc, kc)
      temp1 = p_free(ic, jc, kc)/temp2
      delta2d = (gamma-1.d0)*delta_infd/(gamma+1.d0) + gamma*2.d0*(&
&       p_freed(ic, jc, kc)-temp1*(rho_free(ic, jc, kc)*(gamma+1.d0)*&
&       delta_infd+(gamma+1.d0)*delta_inf*rho_freed(ic, jc, kc)))/temp2
      delta2 = (gamma-1.d0)*(delta_inf/(gamma+1.d0)) + gamma*2.d0*temp1
!Velocity normal to the shock after the shock
      sud(ic, jc, kc) = u_freed(ic, jc, kc) + shocknormalx(ic, kc)*(&
&       delta2d-delta_infd) + (delta2-delta_inf)*shocknormalxd(ic, kc)
      su(ic, jc, kc) = u_free(ic, jc, kc) + (delta2-delta_inf)*&
&       shocknormalx(ic, kc)
      svd(ic, jc, kc) = v_freed(ic, jc, kc) + shocknormaly(ic, kc)*(&
&       delta2d-delta_infd) + (delta2-delta_inf)*shocknormalyd(ic, kc)
      sv(ic, jc, kc) = v_free(ic, jc, kc) + (delta2-delta_inf)*&
&       shocknormaly(ic, kc)
      swd(ic, jc, kc) = w_freed(ic, jc, kc) + shocknormalz(ic, kc)*(&
&       delta2d-delta_infd) + (delta2-delta_inf)*shocknormalzd(ic, kc)
      sw(ic, jc, kc) = w_free(ic, jc, kc) + (delta2-delta_inf)*&
&       shocknormalz(ic, kc)
      temp2 = delta_inf*delta_inf/(gamma+1.0)
      spd(ic, jc, kc) = 2.0*(temp2*rho_freed(ic, jc, kc)+rho_free(ic, jc&
&       , kc)*2*delta_inf*delta_infd/(gamma+1.0)) - (gamma-1.0)*p_freed(&
&       ic, jc, kc)/(gamma+1.0)
      sp(ic, jc, kc) = 2.0*(rho_free(ic, jc, kc)*temp2) - (gamma-1.0)*(&
&       p_free(ic, jc, kc)/(gamma+1.0))
      temp2 = delta_inf/delta2
      srhod(ic, jc, kc) = temp2*rho_freed(ic, jc, kc) + rho_free(ic, jc&
&       , kc)*(delta_infd-temp2*delta2d)/delta2
      srho(ic, jc, kc) = rho_free(ic, jc, kc)*temp2
      temp2 = gamma*(mach_ref*mach_ref)
      temp1 = sp(ic, jc, kc)/srho(ic, jc, kc)
      std(ic, jc, kc) = temp2*(spd(ic, jc, kc)-temp1*srhod(ic, jc, kc))/&
&       srho(ic, jc, kc)
      st(ic, jc, kc) = temp2*temp1
    END DO
  END DO
  
  DEALLOCATE(x_grid)
  DEALLOCATE(y_grid)
  DEALLOCATE(z_grid)
  IF (ALLOCATED(shockhdxid)) THEN
    DEALLOCATE(shockhdxid)
  END IF
  DEALLOCATE(shockhdxi)
  IF (ALLOCATED(shockhdzetad)) THEN
    DEALLOCATE(shockhdzetad)
  END IF
  DEALLOCATE(shockhdzeta)
  IF (ALLOCATED(shockvdxid)) THEN
    DEALLOCATE(shockvdxid)
  END IF
  DEALLOCATE(shockvdxi)
  IF (ALLOCATED(shockvdzetad)) THEN
    DEALLOCATE(shockvdzetad)
  END IF
  DEALLOCATE(shockvdzeta)
  IF (ALLOCATED(dxdxid)) THEN
    DEALLOCATE(dxdxid)
  END IF
  DEALLOCATE(dxdxi)
  IF (ALLOCATED(dxdetad)) THEN
    DEALLOCATE(dxdetad)
  END IF
  DEALLOCATE(dxdeta)
  IF (ALLOCATED(dxdzetad)) THEN
    DEALLOCATE(dxdzetad)
  END IF
  DEALLOCATE(dxdzeta)
  IF (ALLOCATED(dydxid)) THEN
    DEALLOCATE(dydxid)
  END IF
  DEALLOCATE(dydxi)
  IF (ALLOCATED(dydetad)) THEN
    DEALLOCATE(dydetad)
  END IF
  DEALLOCATE(dydeta)
  IF (ALLOCATED(dydzetad)) THEN
    DEALLOCATE(dydzetad)
  END IF
  DEALLOCATE(dydzeta)
  IF (ALLOCATED(dzdxid)) THEN
    DEALLOCATE(dzdxid)
  END IF
  DEALLOCATE(dzdxi)
  IF (ALLOCATED(dzdetad)) THEN
    DEALLOCATE(dzdetad)
  END IF
  DEALLOCATE(dzdeta)
  IF (ALLOCATED(dzdzetad)) THEN
    DEALLOCATE(dzdzetad)
  END IF
  DEALLOCATE(dzdzeta)
  IF (ALLOCATED(jacod)) THEN
    DEALLOCATE(jacod)
  END IF
  DEALLOCATE(jaco)
  DEALLOCATE(invjacodt)
  DEALLOCATE(dxidx)
  DEALLOCATE(dxidy)
  DEALLOCATE(dxidz)
  IF (ALLOCATED(detadxd)) THEN
    DEALLOCATE(detadxd)
  END IF
  DEALLOCATE(detadx)
  IF (ALLOCATED(detadyd)) THEN
    DEALLOCATE(detadyd)
  END IF
  DEALLOCATE(detady)
  IF (ALLOCATED(detadzd)) THEN
    DEALLOCATE(detadzd)
  END IF
  DEALLOCATE(detadz)
  DEALLOCATE(dzetadx)
  DEALLOCATE(dzetady)
  DEALLOCATE(dzetadz)
  DEALLOCATE(nablaxi)
  IF (ALLOCATED(nablaetad)) THEN
    DEALLOCATE(nablaetad)
  END IF
  DEALLOCATE(nablaeta)
  DEALLOCATE(nablazeta)
  IF (ALLOCATED(shocknormalxd)) THEN
    DEALLOCATE(shocknormalxd)
  END IF
  DEALLOCATE(shocknormalx)
  IF (ALLOCATED(shocknormalyd)) THEN
    DEALLOCATE(shocknormalyd)
  END IF
  DEALLOCATE(shocknormaly)
  IF (ALLOCATED(shocknormalzd)) THEN
    DEALLOCATE(shocknormalzd)
  END IF
  DEALLOCATE(shocknormalz)
END SUBROUTINE LINEARIZED_SHOCKBC_D

!  Differentiation of calculate_fdxi_surf_ce_bc in forward (tangent) mode:
!   variations   of useful results: df
!   with respect to varying inputs: f
SUBROUTINE CALCULATE_FDXI_SURF_CE_D_un_BC(df, dfd, f, fd, nx, nz, overlap, npx, npx0)
  IMPLICIT NONE
  INTEGER, INTENT(IN) :: nx, nz, overlap, npx, npx0
  REAL*8, INTENT(IN) :: f(1-overlap:nx+overlap, 1-overlap:nz+overlap)
  REAL*8, INTENT(IN) :: fd(1-overlap:nx+overlap, 1-overlap:nz+overlap)
  REAL*8, INTENT(OUT) :: df(1-overlap:nx+overlap, 1-overlap:nz+overlap)
  REAL*8, INTENT(OUT) :: dfd(1-overlap:nx+overlap, 1-overlap:nz+overlap)
  INTEGER*4 :: ic, kc
  df = 0.d0
  dfd = 0.0_8
  DO kc=1,nz
    DO ic=1,nx
      dfd(ic, kc) = 9.d0*fd(ic-2, kc) - fd(ic-3, kc) - 45.d0*fd(ic-1, kc) + &
&       45.d0*fd(ic+1, kc) - 9.d0*fd(ic+2, kc) + fd(ic+3, kc)
      df(ic, kc) = -(1.d0*f(ic-3, kc)) + 9.d0*f(ic-2, kc) - 45.d0*f(ic-1, kc) + &
&       45.d0*f(ic+1, kc) - 9.d0*f(ic+2, kc) + f(ic+3, kc)
      dfd(ic, kc) = dfd(ic, kc)/60.d0
      df(ic, kc) = df(ic, kc)/60.d0
    END DO
  END DO
  if (npx == 0)then
  DO kc=1,nz
    ic = 1
    dfd(ic, kc) = (240*fd(ic+1, kc)-125.d0*fd(ic, kc)-180.d0*fd(ic+2, kc)+80.d0*&
&     fd(ic+3, kc)-15.d0*fd(ic+4, kc))/60.d0
    df(ic, kc) = (-(125.d0*f(ic, kc))+240*f(ic+1, kc)-180.d0*f(ic+2, kc)+80.d0*f&
&     (ic+3, kc)-15.d0*f(ic+4, kc))/60.d0
    ic = 2
    dfd(ic, kc) = (90.d0*fd(ic+1, kc)-50.d0*fd(ic, kc)-15.d0*fd(ic-1,kc)+5.d0*&
&     fd(ic+3, kc)-30.d0*fd(ic+2, kc))/60.d0
    df(ic, kc) = (-(15.d0*f(ic-1,kc))-50.d0*f(ic, kc)+90.d0*f(ic+1, kc)-30.d0*f&
&     (ic+2, kc)+5.d0*f(ic+3, kc))/60.d0
    ic = 3
    dfd(ic, kc) = (5.d0*fd(ic-2,kc)-40.d0*fd(ic-1,kc)+40.d0*fd(ic+1, kc)-5.d0*&
&     fd(ic+2, kc))/60.d0
    df(ic, kc) = (5.d0*f(ic-2,kc)-40.d0*f(ic-1,kc)+0.d0*f(ic, kc)+40.d0*f(ic+1&
&     , kc)-5.d0*f(ic+2, kc))/60.d0
  END DO
  END IF
  
  if (npx == npx0 - 1)then
  DO kc=1,nz
    ic = nx - 2
    dfd(ic, kc) = (40.d0*fd(ic+1, kc)-5.d0*fd(ic+2, kc)+5.d0*fd(ic-2,kc)-40.d0*&
&     fd(ic-1,kc))/60.d0
    df(ic, kc) = (-(5.d0*f(ic+2, kc))+40.d0*f(ic+1, kc)+0.d0*f(ic, kc)-40.d0*f(ic&
&     -1, kc)+5.d0*f(ic-2,kc))/60.d0
    ic = nx - 1
    dfd(ic, kc) = (15.d0*fd(ic+1, kc)+50.d0*fd(ic, kc)-90.d0*fd(ic-1,kc)+30.d0*&
&     fd(ic-2,kc)-5.d0*fd(ic-3, kc))/60.d0
