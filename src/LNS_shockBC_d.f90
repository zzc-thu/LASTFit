END SUBROUTINE CALCULATE_FDXI_SURF_CE_D_un_BC

  !Differentiation of calculate_fdzeta_surf_ce in forward (tangent) mode:
  ! variations   of useful results: df
  ! with respect to varying inputs: f
SUBROUTINE CALCULATE_FDZETA_SURF_CE_D_un_BC(df, dfd, f, fd, nx, nz, overlap, npz, npz0)
  IMPLICIT NONE
  INTEGER, INTENT(IN) :: nx, nz, overlap, npz, npz0
  REAL*8, INTENT(IN) :: f(1-overlap:nx+overlap, 1-overlap:nz+overlap)
  REAL*8, INTENT(IN) :: fd(1-overlap:nx+overlap, 1-overlap:nz+overlap)
  REAL*8, INTENT(OUT) :: df(1-overlap:nx+overlap, 1-overlap:nz+overlap)
  REAL*8, INTENT(OUT) :: dfd(1-overlap:nx+overlap, 1-overlap:nz+overlap)
  INTEGER :: ic, kc
  df = 0.d0
  dfd = 0.0_8
  DO kc=1,nz
    DO ic=1,nx
      dfd(ic, kc) = 9.d0*fd(ic, kc-2) - fd(ic, kc-3) - 45.d0*fd(ic, kc-1) + &
&       45.d0*fd(ic, kc+1) - 9.d0*fd(ic, kc+2) + fd(ic, kc+3)
      df(ic, kc) = -(1.d0*f(ic, kc-3)) + 9.d0*f(ic, kc-2) - 45.d0*f(ic, kc-1) + &
&       45.d0*f(ic, kc+1) - 9.d0*f(ic, kc+2) + f(ic, kc+3)
      dfd(ic, kc) = dfd(ic, kc)/60.d0
      df(ic, kc) = df(ic, kc)/60.d0
    END DO
  END DO
  
  if (npz == 0)then
  DO ic=1,nx
    kc = 1
    dfd(ic, kc) = (240*fd(ic, kc+1)-125.d0*fd(ic, kc)-180.d0*fd(ic, kc+2)+80.d0*&
&     fd(ic, kc+3)-15.d0*fd(ic, kc+4))/60.d0
    df(ic, kc) = (-(125.d0*f(ic, kc))+240*f(ic, kc+1)-180.d0*f(ic, kc+2)+80.d0*f&
&     (ic, kc+3)-15.d0*f(ic, kc+4))/60.d0
    kc = 2
    dfd(ic, kc) = (90.d0*fd(ic, kc+1)-50.d0*fd(ic, kc)-15.d0*fd(ic, kc-1)+5.d0*&
&     fd(ic, kc+3)-30.d0*fd(ic, kc+2))/60.d0
    df(ic, kc) = (-(15.d0*f(ic, kc-1))-50.d0*f(ic, kc)+90.d0*f(ic, kc+1)-30.d0*f&
&     (ic, kc+2)+5.d0*f(ic, kc+3))/60.d0
    kc = 3
    dfd(ic, kc) = (5.d0*fd(ic, kc-2)-40.d0*fd(ic, kc-1)+40.d0*fd(ic, kc+1)-5.d0*&
&     fd(ic, kc+2))/60.d0
    df(ic, kc) = (5.d0*f(ic, kc-2)-40.d0*f(ic, kc-1)+0.d0*f(ic, kc)+40.d0*f(ic, kc&
&     +1)-5.d0*f(ic, kc+2))/60.d0
  END DO
  END IF
  
  if (npz == npz0 - 1)then
  DO ic=1,nx
    kc = nz - 2
    dfd(ic, kc) = (40.d0*fd(ic, kc+1)-5.d0*fd(ic, kc+2)+5.d0*fd(ic, kc-2)-40.d0*&
&     fd(ic, kc-1))/60.d0
    df(ic, kc) = (-(5.d0*f(ic, kc+2))+40.d0*f(ic, kc+1)+0.d0*f(ic, kc)-40.d0*f(ic&
&     , kc-1)+5.d0*f(ic, kc-2))/60.d0
    kc = nz - 1
    dfd(ic, kc) = (15.d0*fd(ic, kc+1)+50.d0*fd(ic, kc)-90.d0*fd(ic, kc-1)+30.d0*&
&     fd(ic, kc-2)-5.d0*fd(ic, kc-3))/60.d0
    df(ic, kc) = (15.d0*f(ic, kc+1)+50.d0*f(ic, kc)-90.d0*f(ic, kc-1)+30.d0*f(ic&
&     , kc-2)-5.d0*f(ic, kc-3))/60.d0
    kc = nz
    dfd(ic, kc) = (125.d0*fd(ic, kc)-240.d0*fd(ic, kc-1)+180.d0*fd(ic, kc-2)+&
&     15.d0*fd(ic, kc-4)-80.d0*fd(ic, kc-3))/60.d0
    df(ic, kc) = (125.d0*f(ic, kc)-240.d0*f(ic, kc-1)+180.d0*f(ic, kc-2)-80.d0*f&
&     (ic, kc-3)+15.d0*f(ic, kc-4))/60.d0
  END DO
  END IF
END SUBROUTINE CALCULATE_FDZETA_SURF_CE_D_un_BC
